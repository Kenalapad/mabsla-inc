// NavBar onhover change background-color to #8DECB4/LightGreen
$(document).ready(function(){
    $(".dropdown-item").hover(function(){
        $(this).css("background-color", "#8DECB4");
        }, function(){
        $(this).css("background-color", "white");
    });
});
// end NavBar onhover change background-color to #8DECB4/LightGreen
